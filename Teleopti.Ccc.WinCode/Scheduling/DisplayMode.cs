namespace Teleopti.Ccc.WinCode.Scheduling
{
    /// <summary>
    /// Enum describing different display options in period view
    /// </summary>
    public enum DisplayMode
    {
        /// <summary>
        /// Whole day
        /// </summary>
        WholeDay,
        /// <summary>
        /// Begins today
        /// </summary>
        BeginsToday,
        /// <summary>
        /// Ends today
        /// </summary>
        EndsToday,
        /// <summary>
        /// Begins and ends today
        /// </summary>
        BeginsAndEndsToday,
        /// <summary>
        /// A day off
        /// </summary>
        DayOff,
        /// <summary>
        /// None
        /// </summary>
        None
    } ;
}